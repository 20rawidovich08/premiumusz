const adminDict: Record<AdminLang, Record<string, string>> = { uz: uzDict, ru: ruDict, en: ruDict };

export function useAdminT() {
  const { lang } = useI18n();
  const current = (lang in adminDict ? lang : "uz") as AdminLang;
  return (key: AdminKey) => adminDict[current][key] || adminDict.uz[key] || key;
}
